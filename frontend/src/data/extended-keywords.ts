export const getKeywordsByCategory = (category: string): string[] => {
  if (category.includes("job")) return STATE_JOBS_EXPANDED;
  if (category.includes("exam")) return DETAILED_EXAM_KEYWORDS;
  if (category.includes("tool")) return COMPREHENSIVE_TOOL_KEYWORDS;
  if (category.includes("voice")) return VOICE_SEARCH_KEYWORDS;
  if (category.includes("transaction")) return TRANSACTIONAL_KEYWORDS;
  if (category.includes("regional")) return REGIONAL_KEYWORDS;
  return EXTENDED_KEYWORDS_COMPLETE;
};

export const getRandomKeywords = (count: number = 10): string[] => {
  const keywords = EXTENDED_KEYWORDS_COMPLETE;
  return keywords.sort(() => Math.random() - 0.5).slice(0, count);
};

export const searchKeywords = (query: string): string[] => {
  const lowerQuery = query.toLowerCase();
  return EXTENDED_KEYWORDS_COMPLETE.filter(kw => 
    kw.toLowerCase().includes(lowerQuery)
  );
};

export const getKeywordVariations = (keyword: string): string[] => {
  const variations: string[] = [keyword];
  
  // Add question variations
  variations.push(`how to ${keyword}`);
  variations.push(`best ${keyword}`);
  variations.push(`free ${keyword}`);
  variations.push(`${keyword} 2026`);
  variations.push(`${keyword} India`);
  variations.push(`${keyword} online`);
  variations.push(`${keyword} free`);
  variations.push(`${keyword} without`);
  variations.push(`${keyword} fast`);
  
  return variations;
};

