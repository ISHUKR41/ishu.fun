export const getKeywordCount = (): number => {
  return getAllKeywords().length;
};

/**
 * Filter keywords by category
 */
export const getKeywordsByCategory = (category: keyof typeof ULTIMATE_MEGA_TIER): string[] => {
  const categoryData = ULTIMATE_MEGA_TIER[category];
  if (Array.isArray(categoryData)) {
    return categoryData;
  }
  return Object.values(categoryData).flatMap(v => Array.isArray(v) ? v : []);
};

/**
 * Search keywords
 */
export const searchKeywords = (query: string): string[] => {
  const allKeywords = getAllKeywords();
  const lowerQuery = query.toLowerCase();
  return allKeywords.filter(keyword => 
    keyword.toLowerCase().includes(lowerQuery)
  );
};
